#include <cstdio>
#include <cmath>

int main(){

	int h, m;
	float a;

	while(scanf("%d:%d",&h,&m)==2 && (h!=0 || m!=0)){
		
		a= fabs((h*30 + m*0.5) - m*6);
		if(a>180) a-= 360;
		if(a<0) a= fabs(a);
		printf("%3.3f\n",a);

	}

	return 0;
}
